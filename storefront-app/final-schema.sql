-- অ্যাপের আসল কোড (API রুট + পেজ) থেকে কলামগুলো মিলিয়ে বানানো হয়েছে।
-- লেখা (insert/update/delete) admin panel থেকে service_role key দিয়ে হয় → RLS bypass করে;
-- তাই শুধু "নিজের ডেটা পড়া"-র policy রাখা হয়েছে (storefront dashboard-এর জন্য)।
